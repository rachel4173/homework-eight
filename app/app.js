function fillPageStuff (pageName) {
    let pageContent = pageName + "Page";
    $("#app").html(eval(pageContent));
}

function initApp() {
    fillPageStuff("home");

    $("nav a").on("click", (e) => {
        e.preventDefault();
        // let btnID = $(e.target).attr("id");
        let btnID = e.currentTarget.id;

        if(btnID == "help") {
            helpMe();
        } else {
            fillPageStuff(btnID);
        }
    });
}

$(document).ready(() => {
    initApp();
});